-- Creating a database retail_store and using it
CREATE DATABASE retail_store;
USE retail_store;

/* We split the flat table and create tables customers, products, sales_rep and orders.*/
CREATE TABLE customers (
    customer_id VARCHAR(50),
    customer_name VARCHAR(50) NOT NULL,
    customer_email VARCHAR(100) NOT NULL,
    customer_city VARCHAR(50) NOT NULL,
    PRIMARY KEY (customer_id)
);

CREATE TABLE products (
    product_id VARCHAR(50),
    product_name VARCHAR(100) NOT NULL,
    category VARCHAR(50),
    unit_price DECIMAL(10,2) NOT NULL,
    PRIMARY KEY (product_id)
);

CREATE TABLE sales_rep (
    sales_rep_id VARCHAR(50),
    sales_rep_name VARCHAR(50) NOT NULL,
    sales_rep_email VARCHAR(100) NOT NULL,
    office_address VARCHAR(255) NOT NULL,
    PRIMARY KEY (sales_rep_id)
);

CREATE TABLE orders (
    order_id VARCHAR(100),
    customer_id VARCHAR(50),
    product_id VARCHAR(50),
    quantity INT NOT NULL,
    order_date DATE NOT NULL,
    sales_rep_id VARCHAR(50),
    PRIMARY KEY (order_id),
    FOREIGN KEY (customer_id) REFERENCES customers(customer_id),
    FOREIGN KEY (product_id) REFERENCES products(product_id),
    FOREIGN KEY (sales_rep_id) REFERENCES sales_rep(sales_rep_id)
);

